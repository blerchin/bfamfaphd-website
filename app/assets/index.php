					<h2>*</h2>
					<p>The data was collected by the National Center for Educational Statistics’ Integrated Postsecondary Education Data System
					(<a href="http://nces.ed.gov/about/">IPEDS</a>), was downloaded by Jackie Armstrong, corrected for
					population growth by Agnes Szanyi and organized in an excel
					spreadsheet, and visualized by Annelie Berner. IPEDS tracks BAs
					and BFAs, MAs and MFAs, and PhDs in the following categories:
					Visual and Performing Arts, General Crafts/Craft Design, Folk Art
					&amp; Artisanry, Dance, Design and Applied Arts, Drama/Theater Arts
					and Stagecraft, Film/Video and Photographic Arts, Music, Fine and
					Studio Arts.</p>


					<p>How many of “us” are there? The question seems simple. The
					National Endowment for the Arts uses U.S. Census data that
					records people who identify their primary occupation and means of
					generating income as “artist.” We are using graduation data
					because it yields the number of people who aspire to be artists,
					as well as the number of people who go into debt for creative
					fields. According to the IPEDS, in the United States alone,
					colleges and universities have graduated 1,827,087 "creatives"
					since 1987. And so this project, inspired by these questions: If
					we produce identities, not just artworks, what does it mean to
					make work when there are more artists than lawyers, doctors, or
					police officers in the United States? Can we, both as graduates
					in creative fields and non-degree artists, dream differently
					about ourselves?</p>
					
