					<h3>
						<p>What might we<br />
						&mdash; 1,827,087 graduates since 1987 &mdash;<br />
						 do together?</p>
					</h3>

						<p>We already do these things together: we learn about art, go
						into debt, circulate ideas, support the economy, and
						(re)produce relationships and identities.</p>

						<p>Can we satisfy our collective aspirations and
						curiosities?</p>

						<p>If people pursue arts degrees out of a drive for community,
						craft, risk, audience, and knowledge, then how might we meet
						these needs and desires together, for a lifetime, not only 2-4
						years?</p> 

						<p>Imagine that this September, instead of matriculating at a
						traditional 4-year school, prospective freshmen and first-year
						MFA students pool the money they would otherwise spend on
						tuition. The class of 2018 (around 100,000 students paying
						$25,576 on average) would have $2,157,600,000 to work
						with.*</p>

						<p>If only 1/2000th of the class of 2018 pools money, say if only
						fifty prospective students band together, this group would have
						$1,078,800 to work with. That is enough to buy a building and
						own it collectively, into perpetuity, to create a lifelong
						school and community land trust.</p>

						<p>Applications for such a lifelong school will open on January 15,
						2014. To inquire about this school in advance, please email
						<a href="mailto:info@bfamfaphd.com">info@bfamfaphd.com</a>.</p>


